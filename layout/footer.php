		$(document).ready(function(){
			$('.add_to_cart_form').on("submit",function(event){
				event.preventDefault();
				var formData = $(this).serialize();
				$.ajax({
					type:'post',
					url:'add_to_cart.php',
					data:formData,
					dataType:'json'
				})
				//using the done promise callback
				.done(function(data){
					if(data){
						if(data.cart_total_quantity){
							$('.cart-total-quantity').text(data.cart_total_quantity);
							$('.cart-total-price').text("$" + data.cart_total_price);
							swal(data.product_name, "is added to cart !", "success");
                        }
                        if(data.new_product){
                            $('.header-cart-wrapitem').append(data.new_product);
                        }
                        $('.header-cart-total').text("Total: $" + data.cart_total_price);
					}
				})
				//using the fail promise callback
				.fail(function(data){

				});
			});
		});
	</script>
